package com.bean;

public class Book {
	private int bid;
	private String book_name,author_name;
	private float price;
	private int getBid() {
		return bid;
	}
	private void setBid(int bid) {
		this.bid = bid;
	}
	private String getBook_name() {
		return book_name;
	}
	private void setBook_name(String book_name) {
		this.book_name = book_name;
	}
	private String getAuthor_name() {
		return author_name;
	}
	private void setAuthor_name(String author_name) {
		this.author_name = author_name;
	}
	private float getPrice() {
		return price;
	}
	private void setPrice(float price) {
		this.price = price;
	}
	
	
	

}
