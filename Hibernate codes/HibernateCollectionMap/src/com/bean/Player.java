package com.bean;

public class Player {

	private int jno;
	private String playerName;
	private float Player_sold_value;
	public int getJno() {
		return jno;
	}
	public void setJno(int jno) {
		this.jno = jno;
	}
	public String getPlayerName() {
		return playerName;
	}
	public void setPlayerName(String playerName) {
		this.playerName = playerName;
	}
	public float getPlayer_sold_value() {
		return Player_sold_value;
	}
	public void setPlayer_sold_value(float player_sold_value) {
		Player_sold_value = player_sold_value;
	}
	
	
	
}
